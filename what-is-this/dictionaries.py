#   DICTIONARIES:
#   - Key : Value pairs
#   - Built-in Data type
#   - Ordered (from python 3.7 up)
#   - Unordered (before python 3.7)
#   - Created using "{}"   
#   - Values are ordered, changeable, no duplicates

dictionary = {
    "brand": "Ford",
    "model": "Focus",
    "year": 2010
}

print(dictionary)

print(dictionary["brand"])
print(dictionary["model"])
print(dictionary["year"])